public class EditorCoordinates {
  private int row;
  private int col;

  /***
   * Creates a new coordinate object with row, col
   * @param row of coordinate
   * @param col of coordinate
   */
  public EditorCoordinates(int row, int col) {
    this.row = row;
    this.col = col;
  }

}
