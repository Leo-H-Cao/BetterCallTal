package oogasalad;

public interface ModifiesRulesState {

  /**
   * Sets game rules that are defined by user
   * @return Game Rules object that contains all the rules set by user
   */
  public GameRules setRules();

  /**
   * Sets special rule for when user presses the given key during game
   * @param key is the keyboard input
   * @param interaction, effect of the key input
   */
  public void addKeyInteraction(Key key, KeyInteraction interaction);

}